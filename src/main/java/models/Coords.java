public class Coords {
    private Double lat;
    private Double lng;

    public Coords(Double lat, Double lng) {
        this.lat = lat;
        this.lng = lng;
    }

    public Double getLat() {
        return lat;
    }



    public Double getLng() {
        return lng;
    }


}
